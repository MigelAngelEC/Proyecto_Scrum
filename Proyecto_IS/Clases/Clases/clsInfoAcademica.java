package Clases;

/**
 *
 * @author migue
 */
public class clsInfoAcademica {

    private String Institución;
    private String titulo;
    private String nivel;
    private String nombre_niv;
    private String especialidad;

    /**
     * @return the Institución
     */
    public String getInstitución() {
        return Institución;
    }

    /**
     * @param Institución the Institución to set
     */
    public void setInstitución(String Institución) {
        this.Institución = Institución;
    }

    /**
     * @return the titulo
     */
    public String getTitulo() {
        return titulo;
    }

    /**
     * @param titulo the titulo to set
     */
    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    /**
     * @return the nivel
     */
    public String getNivel() {
        return nivel;
    }

    /**
     * @param nivel the nivel to set
     */
    public void setNivel(String nivel) {
        this.nivel = nivel;
    }

    /**
     * @return the nombre_niv
     */
    public String getNombre_niv() {
        return nombre_niv;
    }

    /**
     * @param nombre_niv the nombre_niv to set
     */
    public void setNombre_niv(String nombre_niv) {
        this.nombre_niv = nombre_niv;
    }

    /**
     * @return the especialidad
     */
    public String getEspecialidad() {
        return especialidad;
    }

    /**
     * @param especialidad the especialidad to set
     */
    public void setEspecialidad(String especialidad) {
        this.especialidad = especialidad;
    }

}
